// 무거운 작업을 잘게 쪼개 이벤트 루프를 막지 않는 예제
// 큰 반복을 한 번에 돌리지 않고, setTimeout으로 여러 틱에 나눠 처리한다

let i = 0; // 지금까지 처리한 총 횟수를 누적하는 카운터

function lightTask() {
    // 목표치(1조)에 도달하면 종료 (재귀 중단 조건)
    if(i >= 1_000_000_000_000) return;

    // 한 번의 호출에서는 100만 번만 처리 → 짧게 끊어 실행
    for(let j = 0; j < 1000_000; j++) i++;

    // 남은 작업을 다음 이벤트 루프 틱으로 넘긴다
    // 이렇게 하면 각 작업 사이에 다른 코드(렌더링, 이벤트 등)가 끼어들 수 있어
    // UI/메인 스레드가 멈추지 않는다 (블로킹 방지)
    setTimeout(lightTask, 0);
}

lightTask(); // 작업 시작